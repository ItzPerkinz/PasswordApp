public class Account {

    private String name;
    private String category;
    private String username;
    private String password;
    private String email;
    private String notes;

    public Account(String n, String c, String u, String p, String e, String no) {
        name = n;
        category = c;
        username = u;
        password = p;
        email = e;
        notes = no;
    }

    

}
